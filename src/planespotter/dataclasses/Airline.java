package planespotter.dataclasses;

/**
 * Airline class: represents an airline
 */
public class Airline {
    private int id;
    private String name;

    public Airline (String name) {
        this.id = id;
        this.name = name;
    }

    /**
     * getter
     */
    public int getID () { return id; }
    public String getName () { return name; }


}
